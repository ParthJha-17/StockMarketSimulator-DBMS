    private void populateUnlistedCompanies() {

        try {
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            String sql = "SELECT Stock_Name FROM IPO_Admin WHERE Status = 0";
            stmt = conn.prepareStatement(sql);
            rs = stmt.executeQuery();

            while (rs.next()) {
                String companyName = rs.getString("Stock_Name");
                UnlistedCompanies.add(companyName);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to fetch unlisted companies: " + e.getMessage());
        } finally {
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
                if (conn != null) conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
