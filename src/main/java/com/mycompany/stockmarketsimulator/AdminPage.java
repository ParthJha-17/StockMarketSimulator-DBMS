    private void AcceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptButtonActionPerformed
        // TODO add your handling code here:
        String selectedCompany = UnlistedCompanies.getSelectedItem();
        if (selectedCompany != null) {
            try {
                conn = DriverManager.getConnection(DB_URL, USER, PASS);
                String updateSql = "UPDATE IPO_Admin SET Status = 1 WHERE Stock_Name = ?";
                stmt = conn.prepareStatement(updateSql);
                stmt.setString(1, selectedCompany);
                int rowsAffected = stmt.executeUpdate();
                
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Company '" + selectedCompany + "' accepted successfully");
                    // Call MoveToStocks() function here
                    moveToStocks();
                    populateUnlistedCompanies();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to accept company '" + selectedCompany + "'");
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "SQL Error: " + e.getMessage());
            } finally {
                try {
                    if (stmt != null) stmt.close();
                    if (conn != null) conn.close();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a company to accept");
        }
    }//GEN-LAST:event_AcceptButtonActionPerformed
    
    private void moveToStocks() throws SQLException {
        // Call MoveToStocks() function in the database
        conn = DriverManager.getConnection(DB_URL, USER, PASS);
        String moveToStocksSql = "{CALL MoveToStocks()}"; // Assuming MoveToStocks() is a stored procedure
        CallableStatement cstmt = conn.prepareCall(moveToStocksSql);
        cstmt.execute();
        JOptionPane.showMessageDialog(this, "MoveToStocks executed successfully");
        cstmt.close();
    }
