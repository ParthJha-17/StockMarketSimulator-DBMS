    private void displayUserStocks() {
        Connection connection = null;
    PreparedStatement stmt = null;
    ResultSet rs = null;

    try {
        // Establish database connection
        connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/smms", "root", "FireRage@007");

        // Prepare SQL query to fetch user's stocks with stock names from joined tables
        String query = "SELECT s.Stock_Name, p.Qt FROM portfolio p " +
                       "INNER JOIN stocks s ON p.Stock_Id = s.Stock_Id " +
                       "WHERE p.User_Id = ?";
        stmt = connection.prepareStatement(query);
        stmt.setInt(1, userID); // Assuming User_Id corresponds to userID

        // Execute query
        rs = stmt.executeQuery();

        // Clear existing list items
        UserStocksList.removeAll();

        // Populate UserStocksList with user's stocks and their names
        while (rs.next()) {
            String stockName = rs.getString("Stock_Name");
            int quantity = rs.getInt("Qt");
            UserStocksList.add(stockName + " - Quantity: " + quantity);
        }

    } catch (SQLException ex) {
        ex.printStackTrace(); // Handle exception appropriately
    } finally {
        // Close resources
        try {
            if (rs != null) rs.close();
            if (stmt != null) stmt.close();
            if (connection != null) connection.close();
        } catch (SQLException ex) {
            ex.printStackTrace(); // Handle exception appropriately
        }
    }
    }

