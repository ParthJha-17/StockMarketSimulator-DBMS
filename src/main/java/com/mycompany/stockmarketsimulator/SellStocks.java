    private void SellButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SellButtonActionPerformed
        // TODO add your handling code here:
         String selectedStock = UserStocksList.getSelectedItem();
        if (selectedStock == null || selectedStock.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a stock to sell.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String[] parts = selectedStock.split(" - Quantity: ");
        String stockName = parts[0];
        int quantity = Integer.parseInt(QuantityToSell.getText().trim());

        if (quantity <= 0) {
            JOptionPane.showMessageDialog(this, "Please enter a valid quantity to sell.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/smms", "root", "FireRage@007");

            // Retrieve Stock_ID based on Stock_Name
            PreparedStatement stockStmt = connection.prepareStatement("SELECT Stock_ID FROM stocks WHERE Stock_Name = ?");
            stockStmt.setString(1, stockName);
            ResultSet stockRs = stockStmt.executeQuery();
            if (stockRs.next()) {
                int stockID = stockRs.getInt("Stock_ID");

                // Insert new order into Orders table
                PreparedStatement orderStmt = connection.prepareStatement(
                        "INSERT INTO Orders (Status, Quantity, Stock_ID, Order_Type, Buy_Sell, User_ID) " +
                        "VALUES (?, ?, ?, 1, 2, ?)");
                orderStmt.setInt(1, 1); // Status = 1 (Assuming status for new orders)
                orderStmt.setInt(2, quantity);
                orderStmt.setInt(3, stockID);
                orderStmt.setInt(4, userID);
                orderStmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Sell order placed successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                displayUserStocks(); // Refresh the displayed user stocks after selling

                orderStmt.close();
            } else {
                JOptionPane.showMessageDialog(this, "Stock not found.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            stockStmt.close();
            connection.close();

        } catch (SQLException ex) {
            ex.printStackTrace(); // Handle exception appropriately
            JOptionPane.showMessageDialog(this, "Error occurred while processing sell order.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SellButtonActionPerformed

