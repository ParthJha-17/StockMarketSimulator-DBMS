    private void StockListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_StockListMouseClicked
        // TODO add your handling code here:
        // TODO add your handling code here:
        String selectedStockName = StockList.getSelectedItem();
        if (selectedStockName != null && !selectedStockName.isEmpty()) {
            try {
                Connection connection = DatabaseConnection.getConnection();
                PreparedStatement getPriceStatement = connection.prepareStatement("SELECT Latest_Price FROM stocks WHERE Stock_Name = ?");
                getPriceStatement.setString(1, selectedStockName);
                ResultSet resultSet = getPriceStatement.executeQuery();

                if (resultSet.next()) {
                    float price = resultSet.getFloat("Latest_Price");
                    int quantity = Integer.parseInt(BuyQuantity.getText());

                    float totalAmountRequired = price * quantity;
                    AmountReq.setText(String.valueOf(totalAmountRequired));
                }

                resultSet.close();
                getPriceStatement.close();
                connection.close();
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid quantity.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_StockListMouseClicked

    private void BuyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuyButtonActionPerformed
        String selectedStockName = StockList.getSelectedItem();

    if (selectedStockName == null || selectedStockName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a stock.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/smms", "root", "FireRage@007");

        // Retrieve the price and Stock_ID of the selected stock
        PreparedStatement getPriceStatement = connection.prepareStatement("SELECT Price, Stock_ID FROM Company_Database WHERE Stock_Name = ?");
        getPriceStatement.setString(1, selectedStockName);
        ResultSet priceResultSet = getPriceStatement.executeQuery();

        if (priceResultSet.next()) {
            float price = priceResultSet.getFloat("Price");
            int stockID = priceResultSet.getInt("Stock_ID"); // Retrieve Stock_ID

            int quantity = Integer.parseInt(BuyQuantity.getText());
            float totalAmountRequired = price * quantity;

            // Retrieve user's available funds
            PreparedStatement getBalanceStatement = connection.prepareStatement("SELECT Funds_Available FROM profile_database WHERE User_ID = ?");
            getBalanceStatement.setInt(1, userID);
            ResultSet balanceResultSet = getBalanceStatement.executeQuery();

            if (balanceResultSet.next()) {
                float currentFunds = balanceResultSet.getFloat("Funds_Available");

                if (currentFunds >= totalAmountRequired) {
                    // Calculate new funds after purchase
                    float newFunds = currentFunds - totalAmountRequired;

                    // Update user's funds
                    PreparedStatement updateBalanceStatement = connection.prepareStatement("UPDATE profile_database SET Funds_Available = ? WHERE User_ID = ?");
                    updateBalanceStatement.setFloat(1, newFunds);
                    updateBalanceStatement.setInt(2, userID);
                    updateBalanceStatement.executeUpdate();

                    // Insert the order into the Orders table (auto-generated Order_ID)
                    PreparedStatement insertOrderStatement = connection.prepareStatement("INSERT INTO Orders (Status, Quantity, Stock_ID, Order_Type, Buy_Sell, User_ID) VALUES (1, ?, ?, 1, 1, ?)", Statement.RETURN_GENERATED_KEYS);
                    insertOrderStatement.setInt(1, quantity);
                    insertOrderStatement.setInt(2, stockID);
                    insertOrderStatement.setInt(3, userID);
                    insertOrderStatement.executeUpdate();

                    // Retrieve the auto-generated Order_ID
                    ResultSet generatedKeys = insertOrderStatement.getGeneratedKeys();
                    if (generatedKeys.next()) {
                        int orderID = generatedKeys.getInt(1);
                        JOptionPane.showMessageDialog(this, "Purchase successful! Order ID: " + orderID, "Success", JOptionPane.INFORMATION_MESSAGE);
                    }

                    generatedKeys.close();
                    insertOrderStatement.close();
                } else {
                    JOptionPane.showMessageDialog(this, "Insufficient funds.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }

            balanceResultSet.close();
            getBalanceStatement.close();
        }

        priceResultSet.close();
        getPriceStatement.close();
        connection.close();
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error purchasing stock: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Invalid quantity.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_BuyButtonActionPerformed

