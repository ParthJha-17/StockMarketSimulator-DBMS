     private void displayAvailableStocks() {
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/smms", "root", "FireRage@007");
            String sql = "SELECT Stock_Name FROM stocks";
            PreparedStatement statement = connection.prepareStatement(sql);
            ResultSet resultSet = statement.executeQuery();

            StockList.removeAll(); // Clear existing items in StockList

            while (resultSet.next()) {
                String stockName = resultSet.getString("Stock_Name");
                StockList.add(stockName);
            }

            resultSet.close();
            statement.close();
            connection.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error retrieving stocks: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
